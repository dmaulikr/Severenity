import UIKit

class ListViewController: UITableViewController {
    
    private var dataForList: NSMutableArray = []
    
    
    func provideDataForList() {
        
        let locationsServerManager = LocationsServerManager()
        locationsServerManager.requestLocationsFromServer { (result) in
            self.dataForList = result.mutableCopy() as! NSMutableArray // if let
            self.tableView.reloadData()
            
        }
    }
    
    override func viewDidLoad() {
        super.viewDidLoad()
        tableView.registerNib(UINib(nibName: "ListCell", bundle: nil), forCellReuseIdentifier: "CellInList")
    
        
        
        
        provideDataForList()
        
        
        
        
        
    }
    
    override func tableView(tableView: UITableView, didSelectRowAtIndexPath indexPath: NSIndexPath) {
        
        tableView.deselectRowAtIndexPath(indexPath, animated: true)
    }


